module Pics1 where
import BitmapOps(bm)
import InvaderTypes

pics1 obj =
  case obj of
    Base       -> base1
    Explode    -> explode1
    Ufo        -> spacer1
    VShot A    -> sperma1
    VShot B    -> spermb1
    Vader R1 A -> vader1a1
    Vader R1 B -> vader1b1
    Vader R2 A -> vader2a1
    Vader R2 B -> vader2b1
    Vader R3 A -> vader3a1
    Vader R3 B -> vader3b1
    VExplode _ -> vexplode1

